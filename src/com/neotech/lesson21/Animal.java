package com.neotech.lesson21;

public class Animal {
	
	//instances variables in Java
	public String color, size; 
	public int age;
	
	
	public void sleep() {
		System.out.println("All the animals sleep");
	}

	
	public void eat() {
		System.out.println("All animals eat");
	}
	
	public void info() {
		System.out.println(size + " " + color + " " + age );
	}
	
	
	
	
}
