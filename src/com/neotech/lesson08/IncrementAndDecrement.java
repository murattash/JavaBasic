package com.neotech.lesson08;

public class IncrementAndDecrement {
	
	public static void main(String[] args) {
		
		int x = 10;
		
		x = x + 1;
		
		//as shortcut 
		x  += 1;  //shorthand operator 
		
		//increment 
		x++;     //same as x = x + 1
		
		System.out.println(x);
		
		//decrement
		int y = 15;   
		
		y = y - 1;	  
		
		//shorthand operator
		y -= 1;
		
		//decrement
		y--;
		
		System.out.println(y);
		
	}
	

}
